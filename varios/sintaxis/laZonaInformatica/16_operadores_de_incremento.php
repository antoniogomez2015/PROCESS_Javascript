	<script type="text/javascript">
	
		/*los operadores de incremento permiten agregar ó sustrar una auniadad a un elemento u objeto*/
		/*los operadores de incremento tienen dos maneras de aplicarse:*/
			/*pre-fija: esta permite agregar y alterar el valor antes de que el elemento sea interpretado*/
			/*pos-fija: esta permite agregar el valor después de que el elemento sea interpretado, así, cabiará el valor del elemento cuando este sea llamado ó impreso posterior mente*/
			
			/*esto es especial mente útil para cuando deseamos cambiar el valor de elemento manteniendo 
			ó nó su estado inicial*/
		
		x = 10;
		
		x++;
		
		document.write(x);
		
		++x;
		
		document.write(x);
	
	</script>

